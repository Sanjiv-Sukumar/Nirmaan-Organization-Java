package bank;

public class Bank {
		private int id = 21;
		private String name = "Sanjiv";
		private int amount = 5000;
		private int pin = 1234;
		
		public Bank() {
			
		}

		public int getId() {
			return id;
		}

		public void setId(int id) {
			this.id = id;
		}

		public String getName() {
			return name;
		}

		public void setName(String name) {
			this.name = name;
		}

		public int getAmount() {
			return amount;
		}

		public void setAmount(int amount) {
			this.amount = amount;
		}
		
		public int getPin() {
			return pin;
		}

		
}
